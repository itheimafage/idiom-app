def main():
    json_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), '..', 'src', 'data', 'exam-questions.json')
    
    with open(json_path, 'r', encoding='utf-8') as f:
        existing = json.load(f)
    
    print(f"当前题目数: {len(existing)}")
    print(f"第五批新增: {len(batch5)}")
    
    existing_stems = set()
    for q in existing:
        stem = q['stem'].strip()
        existing_stems.add(stem[:60])
    
    new_count = 0
    skip_count = 0
    for q in batch5:
        stem = q['stem'].strip()
        if stem[:60] not in existing_stems:
            existing.append(q)
            existing_stems.add(stem[:60])
            new_count += 1
        else:
            skip_count += 1
    
    print(f"成功新增: {new_count}")
    print(f"重复跳过: {skip_count}")
    print(f"最终总数: {len(existing)}")
    
    sources = {}
    for q in existing:
        s = q.get('source', 'unknown')
        sources[s] = sources.get(s, 0) + 1
    for k, v in sorted(sources.items(), key=lambda x: -x[1]):
        print(f"  {k}: {v}")
    
    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(existing, f, ensure_ascii=False, indent=2)
    
    print(f"\n已保存!")

if __name__ == '__main__':
    main()
